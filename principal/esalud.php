                      eSalud supone una “transformación radical de la sanidad y, por ello, es necesario una evaluación de la
                      eficacia y la seguridad de los sistemas de eSalud”, con el objetivo de que los profesionales sanitarios “estén preparados y, los datos proporcionados por los dispositivos de monitorización, puedan integrarse en la asistencia sanitaria”.
                      </p>
                      <p>
                      Según la Organización Mundial de la Salud (OMS), la cibersalud (conocida también como eSalud o ehealth) consiste "en el apoyo que la utilización costo-eficaz y segura de las tecnologías de la información y las comunicaciones ofrece a la salud y a los ámbitos relacionados con ella, con inclusión de los servicios de atención de salud, la vigilancia y la documentación sanitarias, así como la educación, los conocimientos y las investigaciones en materia de salud".
